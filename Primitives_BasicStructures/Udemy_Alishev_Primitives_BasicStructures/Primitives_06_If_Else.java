package Primitives_BasicStructures.Udemy_Alishev_Primitives_BasicStructures;

public class Primitives_06_If_Else {
    public static void main(String[] args) {
        int myInt = 15;
        if (myInt <= 10) {
            System.out.println("Yes, that's right");
        } else if (myInt > 20) {
            System.out.println("No, it isn't");
        } else {
            System.out.println("None of the previous options");
        }
    }
}
